package Sibunsirpractice;
import java.util.*;
public class Prog5 {
    public static void main(String[] args) {
        System.out.print("Multiplication table\n");
        for(int i=1; i<=15; i++){
            for(int j=2; j<=15; j++){
                System.out.print(i*j+"\t");
            }
            System.out.println();
        }
    }
}
